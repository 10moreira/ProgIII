    private void atualizaTabela() {
             listCidades.clear();
        listCidades.addAll(daocidade.getLista());
        int linha = listCidades.size() - 1;
        if (linha >= 0) {
            jTableCidades.setRowSelectionInterval(linha, linha);
            jTableCidades.scrollRectToVisible(
                    jTableCidades.getCellRect(linha, linha, true));
        }
    }

    private boolean validaCampos() {
        if (!(jTextFieldCidade.getText().length() > 0 && jTextFieldCidade.getText() != null)) {
            JOptionPane.showMessageDialog(null, "O campo cidade deve ser informado!!");
            jTextFieldCidade.requestFocus();
            return false;    
       }
      
        return true;
    }
