    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        //Grava o objeto novo iserido na minha lista.
        if (validaCampos()) {
            int linhaSelecionada = jTableClientes.getSelectedRow();
            Cliente obj = listClientes.get(linhaSelecionada);       
            daocliente.Salvar(obj);
            trataEdicao(false);
            atualizaTabela();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        //Exclui o objeto ja iserido na minha lista.
        int opcao = JOptionPane.showOptionDialog(null, "Confirma a exclusão?", "Pergunta",
                        JOptionPane.YES_NO_OPTION,
                        JOptionPane.QUESTION_MESSAGE, null,
                        new String[]{"Sim", "Não"}, "Sim");
        if (opcao == 0) {
            int linhaSelecionada = jTableClientes.getSelectedRow();
            Cliente obj = listClientes.get(linhaSelecionada);
            daocliente.remover(obj);
            atualizaTabela();
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

