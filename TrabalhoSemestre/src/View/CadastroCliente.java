    //ativa e desatica componentesna minha tela
    private void trataEdicao(boolean editando) {
        btnCancelar.setEnabled(editando);
        btnSalvar.setEnabled(editando);
        btnEditar.setEnabled(!editando);
        btnExcluir.setEnabled(!editando);
        btnNovo.setEnabled(!editando);
        jTableClientes.setEnabled(!editando);
    }
     //Atualiza a minha tabela com as operações.
    private void atualizaTabela() {
        listClientes.clear();
        listClientes.addAll(daocliente.getLista());
        int linha = listClientes.size() - 1;
        if (linha >= 0) {
            jTableClientes.setRowSelectionInterval(linha, linha);
            jTableClientes.scrollRectToVisible(
                    jTableClientes.getCellRect(linha, linha, true));
        }
    }
