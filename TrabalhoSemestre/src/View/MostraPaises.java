private void atualizaTabela() {
        listPaises.clear();
        listPaises.addAll(daopaises.getLista());
        int linha = listPaises.size() - 1;
        if (linha >= 0) {
           jTablePaises.setRowSelectionInterval(linha, linha);
            jTablePaises.scrollRectToVisible(jTablePaises.getCellRect(linha, linha, true));
        }
    }
