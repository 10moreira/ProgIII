    private void atualizaTabela() {
        listEstados.clear();
        listEstados.addAll(daoestado.getLista());
        int linha = listEstados.size() - 1;
        if (linha >= 0) {
            jTableEstados.setRowSelectionInterval(linha, linha);
            jTableEstados.scrollRectToVisible(
                    jTableEstados.getCellRect(linha, linha, true));
        }
    }

    private boolean validaCampos() {
       
       if (!(jTextFieldnome.getText().length() > 0 && jTextFieldnome.getText() != null)) {
            JOptionPane.showMessageDialog(null, "O campo nome deve ser informado!!");
            jTextFieldnome.requestFocus();
            return false;    
       }
       return true;
    }

